    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        
        
        String namep= pacientenombre.getText();
        String detailp= detallep.getText();
        String fecha= fechanacimientopaciente.getText();
        Pacientes paciente1= new Pacientes();
        paciente1.setDetallepadecimiento(detailp);
        paciente1.setFechanacimiento(fecha);
        paciente1.setNombre(namep);
        int i= tipopadecimiento.getSelectedIndex();
        if (i==0){
            paciente1.setTipopadecimento(tipopadecimientos.I);
        }
        if (i==1){
            paciente1.setTipopadecimento(tipopadecimientos.H);
        }
        if (i==2){
            paciente1.setTipopadecimento(tipopadecimientos.P);
        }
        if (i==3){
            paciente1.setTipopadecimento(tipopadecimientos.Q);
        }
        if (i==4){
            paciente1.setTipopadecimento(tipopadecimientos.O);
        }
        if (i==0||i==1){
            paciente1.setColor(colores.ROJO);
        }
        if (i==3 || i==2){
            paciente1.setColor(colores.AMARILLO);
        }
        if (i==4){
            paciente1.setColor(colores.VERDE);
        }
        if (paciente1.getColor()==colores.VERDE){
            HospitalManager.getInstance().agregarcolaprioridadv(paciente1);
        }
        if (paciente1.getColor()==colores.ROJO){
            HospitalManager.getInstance().agregarcolaprioridadr(paciente1);
        }
        if (paciente1.getColor()==colores.AMARILLO){
            HospitalManager.getInstance().agregarcolaprioridada(paciente1);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

